			<h1>R&eacute;alisations techniques</h1>
			<div class="bkg">
				<p>Coeur même de la communication à l'heure actuelle, les sites internets sont désormais indissociables de la communication d'entreprise, encore faut il utiliser ceux qui correspondent à vos besoins.</p>
				<p>Partant de se constat, je suis à même de travailler plusieurs types de sites : les sites vitrines, les sites dynamiques, les blogs ou encore les boutiques en ligne.</p>
				<p>En tant que chargé de projet, je compare ces différents formats afin de d&eacute;terminer quelle solution sera la plus adaptée à vos besoins.</p>
				<p>Voici un petit &eacute;chantillon des productions que j&rsquo; ai r&eacute;alis&eacute; personnellement ou pour le compte de différentes sociétes.</p>
				<p>Il est à noter que tout ne peut pas être montré ici, étant donné le nombre de sites internet crées pour les hôtels et restaurants au sein des Ateliers Apicius, ou du fait que la direction de FPC Group ne souhaite pas que je diffuse les sites que j'ai créer pour l'entreprise.</p>
